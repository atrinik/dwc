/**
 * @file
 * Image related structures. */

#ifndef IMAGE_H
#define IMAGE_H

/** Maximum number of default bmaps hashtable. */
#define BMAPS_MAX 32767

/** Structure for bmap data. */
typedef struct bmap_struct
{
	/** The name. */
	char *name;

	/** Size in bytes. */
	size_t len;

	/** Position in atrinik.p0 file, if -1, it doesn't exist in the file. */
	int pos;

	/** Checksum. */
	unsigned long crc32;
} bmap_struct;

#endif
